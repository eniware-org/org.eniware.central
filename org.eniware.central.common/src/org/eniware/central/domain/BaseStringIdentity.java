package org.eniware.central.domain;

import java.io.Serializable;

/**
 * Base implementation of a String-based {@link Identity}.
 * @version $Revision$
 */
public abstract class BaseStringIdentity implements Cloneable, Serializable, Identity<String> {

	private static final long serialVersionUID = -2979855366308936650L;

	private String id = null;

	@Override
	public Object clone() {
		try {
			return super.clone();
		} catch ( CloneNotSupportedException e ) {
			// should never get here
			throw new RuntimeException(e);
		}
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	/**
	 * Test if two BaseStringEntity objects have the same {@link #getId()}
	 * value.
	 */
	@Override
	public boolean equals(Object obj) {
		if ( this == obj ) {
			return true;
		}
		if ( obj == null ) {
			return false;
		}
		if ( getClass() != obj.getClass() ) {
			return false;
		}
		BaseStringIdentity other = (BaseStringIdentity) obj;
		if ( id == null ) {
			if ( other.id != null ) {
				return false;
			}
		} else if ( !id.equals(other.id) ) {
			return false;
		}
		return true;
	}

	/**
	 * Compare based on the primary key, with <em>null<em> values ordered before
	 * non-null values.
	 */
	@Override
	public int compareTo(String o) {
		if ( id == null && o == null ) {
			return 0;
		}
		if ( id == null ) {
			return -1;
		}
		if ( o == null ) {
			return 1;
		}
		return id.compareTo(o);
	}

	/**
	 * @return the id
	 */
	@Override
	public String getId() {
		return id;
	}

	/**
	 * @param id
	 *        the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

}
